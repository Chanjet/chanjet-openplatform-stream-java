#!/bin/bash
# cjtCli AI 搜索能力深度评估脚本

CLI_BIN="./cli/cjtCli/cjtCli"
if [ ! -f "$CLI_BIN" ]; then
    echo "❌ 找不到 cjtCli 二进制文件，请先编译。"
    exit 1
fi

# 清理旧索引确保测试纯净性
rm -f ~/.cjtCli/*.idx ~/.cjtCli/*.json
$CLI_BIN api list > /dev/null

echo "==============================================================="
echo "        cjtCli 真·AI 语义搜索能力深度评估报告"
echo "==============================================================="

run_test() {
    local title=$1
    local query=$2
    echo -e "\n📌 [场景测试]: $title"
    echo "🔍 [用户输入]: \"$query\""
    echo "---------------------------------------------------------------"
    $CLI_BIN api list --search "$query" | head -n 8
    echo "---------------------------------------------------------------"
}

# 1. 维度一：精准关键词匹配 (Baseline)
run_test "精准匹配 - 发票管理" "增值税电子发票"

# 2. 维度二：核心业务同义词召回 (Core AI Ability)
# 测试“钱”是否能自动关联到“余额/资金/支付”
run_test "语义同义词 - 资金查询" "我钱包里的钱还有多少"

# 3. 维度三：业务意图联想 (Logical Association)
# 测试“卖完了/补货”是否能关联到“库存预警/可用量”
run_test "逻辑联想 - 供应链预警" "货快卖完了怎么办"

# 4. 维度四：模糊动作描述 (Action-Oriented)
# 测试“开账号”是否能关联到“角色分配/资料同步”
run_test "模糊动作 - 组织权限管理" "帮我给新来的员工分一下权限"

# 5. 维度五：长句自然语言理解 (Natural Language)
# 测试复杂长句的抗噪音能力
run_test "长句理解 - 财务对账实战" "我想要核对一下昨天的流水账单并把凭证同步了"

# 6. 维度六：极端语义错位 (Zero Overlap)
# 测试“活着”是否能通过神经激活定位到“健康探测/心跳/存活”
run_test "极端语义 - 系统可观测性" "微服务都还活着吗"

echo -e "\n✅ 评估完成！请对照 API 列表核实召回项的业务相关度。"
